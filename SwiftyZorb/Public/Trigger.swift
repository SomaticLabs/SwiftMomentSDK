import Foundation

// MARK: - Triggerable Patterns

/**
 Enumeration of predefined triggerable patterns
 */
public enum Trigger: String {
    
    // Confetti
    case 🎊 = "p"
    
    // Point left
    case 👈 = "l"
    
    // Point right
    case 👉 = "r"
    
    // Left fist
    case 🤛 = "s"
    
    // Right fist
    case 🤜 = "t"
    
    // Hands raised
    case 🙌 = "u"
    
    // Waiving hand
    case 👋 = "a"
    
    // Hushed
    case 😯 = "q"
    
    // Flushed
    case 😳 = "w"
    
    // Grimacing
    case 😬 = "f"
    
    // Smiling
    case 😊 = "d"
    
    // Grinning
    case 😄 = "m"
    
    // Laughing
    case 🤣 = "k"
}
